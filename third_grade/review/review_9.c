#include <stdio.h>
#include <time.h>

#define N 1000000
#define STEP (N / 1000)

int is_prime(int n);

int main(void) {
  clock_t start = clock(); // 開始時間

  int i = 1;
  while (i <= N) {
    int flag = 0;
    for (int j = i; j <= i * 2; j++) {
      if (is_prime(j)) {
        flag = 1;

        // jが素数ならばjまでの数で定理は成立
        i = j + 1;
        break;
      }
    }

    if (!flag) {
      printf("定理は不成立!!\n");
      return 0;
    }

    if (i % STEP == 0) {
      printf("n=%dまで成立 ", i);
      printf("%f sec\n", (double)(clock() - start) / CLOCKS_PER_SEC);
    }
  }

  printf("%dまでの正数で定理は成立\n", N);
  printf("%f sec\n", (double)(clock() - start) / CLOCKS_PER_SEC);

  return 0;
}

int is_prime(int n) {
  if (n < 2)
    return 0;
  if (n == 2)
    return 1;
  if (n % 2 == 0)
    return 0;

  for (int i = 3; i * i <= n; i += 2) {
    if (n % i == 0)
      return 0;
  }

  return 1;
}

