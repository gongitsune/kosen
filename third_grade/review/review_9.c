#include <stdio.h>
#include <time.h>

#define N 1000000
#define STEP 1000

int is_prime(int n);

int main(void) {
  clock_t start = clock(); // 開始時間

  // Nまでの素数の個数は < 1.25506 * N / log(N) である
  // int primes[(int)(1.25506 * N / log(N))];
  int step = 0;

  int i = 1;
  while (i <= N) {
    int flag = 0;
    for (int j = i * 2; j >= i; j--) {
      if (is_prime(j)) {
        flag = 1;

        // jが素数ならばjまでの数で定理は成立
        // しているためスキップして j+1 から再開
        i = j;
        break;
      }
    }

    if (!flag) {
      printf("定理は不成立!!\n");
      return 0;
    }

    if (i - step >= STEP) {
      printf("n=%dまで成立 ", i);
      printf("%f sec\n", (double)(clock() - start) / CLOCKS_PER_SEC);

      step = i;
    }
  }

  printf("%dまでの正数で定理は成立\n", N);
  printf("%f sec\n", (double)(clock() - start) / CLOCKS_PER_SEC);

  return 0;
}

int is_prime(int n) {
  if (n < 2)
    return 0;

  for (int i = 2; i * i <= n; i++) {
    if (n % i == 0)
      return 0;
  }

  return 1;
}

