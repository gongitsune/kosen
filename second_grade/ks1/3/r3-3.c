#include <stdio.h>

int main(void) {
  float x;
  printf("    x      x**2      x**3      x**4\n");
  for (x = 0.5; x <= 10.0; x += 0.5) {
    float x1 = x * x, x2 = x1 * x, x3 = x2 * x;
    printf("%5.1f%10.2f%10.2f%10.2f\n", x, x1, x2, x3);
  }
  return 0;
}

