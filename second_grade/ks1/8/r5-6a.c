/* 23 豊島圭吾
 * レポート8 プログラム2
 */

#include <stdio.h>

int main(void) {
  char *month[] = {"Jan", "Feb", "Mar", "Apr", "May", "Jun",
                   "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"};

  int d[5][12], total[12];

  int i, r;
  for (i = 0; i < 5; i++) {
    total[i] = 0;
    for (r = 0; r < 12; r++) {
      scanf("%d", &d[i][r]);
    }
  }

  for (r = 0; r < 12; r++) {
    total[r] = 0;
    for (i = 0; i < 5; i++) {
      total[r] += d[i][r];
    }
  }

  printf("      ");
  for (i = 0; i < 12; i++)
    printf("%s ", month[i]);
  printf("\n");

  for (i = 0; i < 5; i++) {
    printf("%4d  ", 1993 + i);
    for (r = 0; r < 12; r++)
      printf("%3d ", d[i][r]);
    printf("\n");
  }
  printf("月計  ");
  for (i = 0; i < 12; i++)
    printf("%3d ", total[i]);
  printf("\n");

  return 0;
}

